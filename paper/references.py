import re
from collections import defaultdict


def _surname(full):
    head = re.match(r"^(.*?)\(\d{4}", full).group(1)
    first = head.split(",")[0].strip()
    return first.rstrip(".")


def _year(full):
    return re.search(r"\((\d{4})", full).group(1)


def _strip_suffix(label):
    return re.sub(r"(\b\d{4})[a-z]\b", r"\1", label)


