def _build_disambiguation():
    groups = defaultdict(list)  # (surname, year) -> [keys]
    for k, (lab, full) in REFERENCES.items():
        groups[(_surname(full), _year(full))].append(k)
    suffix = {}
    for (sn, yr), keys in groups.items():
        if len(keys) == 1:
            suffix[keys[0]] = ""
        else:
            ordered = sorted(keys, key=lambda kk: REFERENCES[kk][1].lower())
            for i, kk in enumerate(ordered):
                suffix[kk] = chr(ord("a") + i)
    return suffix


_SUFFIX = _build_disambiguation()


def intext(key):
    lab = _strip_suffix(REFERENCES[key][0])
    s = _SUFFIX[key]
    if s:
        lab = re.sub(r"(\b\d{4})\b", r"\1" + s, lab)
    return lab


def _ref_with_suffix(key):
    full = REFERENCES[key][1]
    s = _SUFFIX[key]
    if s:
        full = re.sub(r"\((\d{4})\)", r"(\1" + s + ")", full, count=1)
    return full


def _collapse_authors(ref):
    """Integrity safeguard: for any entry whose author block was truncated with
    'et al.', keep ONLY the verified first author plus 'et al.', so that no
    reconstructed (and possibly inaccurate) co-author names are asserted.
    Entries with a fully enumerated author list (no 'et al.') are left intact."""
    if ", et al." not in ref:
        return ref
    head, rest = ref.split(" (", 1)            # head = author block
    parts = head.split(", ")
    first = parts[0] + ", " + parts[1] if len(parts) >= 2 else parts[0]
    return f"{first}, et al. ({rest}"


def sorted_reference_strings():
    return sorted((_collapse_authors(_ref_with_suffix(k)) for k in REFERENCES),
                  key=lambda s: s.lower())
